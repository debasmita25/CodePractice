package com.selfPractice.programs;

public class DataTypesExercise {
	
	public static void main(String[] args) {
		int x,y,z=100;
		
		System.out.println("z " +z); // x and y not initialized
		
		int a=19,b=12,c=90;
		
		System.out.println("a "+a);
		
		float var_1=(float) 12.98d; // Narrowing or Explicit type casting
		System.out.println(var_1);
		
		long var_long;
		var_long='z'; //A-Z = 65 to 90 , a-z = 97 to 122
		System.out.println(var_long);
		
	}

}
